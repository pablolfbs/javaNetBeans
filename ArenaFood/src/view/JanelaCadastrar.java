    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        RestauranteDao rDao = new RestauranteDao();
        Restaurante r = new Restaurante();
        
        if( !"".equals(fieldTipo.getText()) && !"".equals(fieldNome.getText()) && !"".equals(fieldEndereco.getText()) && !"".equals(fieldContato.getText())) {
            r.setTipo(fieldTipo.getText());
            r.setNome(fieldNome.getText());
            r.setEndereco(fieldEndereco.getText());
            r.setContato(fieldContato.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Digite todos os campos!");
            return;
        }

        rDao.cadastrar(r);

        JOptionPane.showMessageDialog(this, "Cadastrado com sucesso");

        fieldTipo.setText("");
        fieldNome.setText("");
        fieldEndereco.setText("");
        fieldContato.setText("");
    }//GEN-LAST:event_btnInserirActionPerformed

