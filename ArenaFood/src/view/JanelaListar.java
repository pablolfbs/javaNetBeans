    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        RestauranteDao rDao = new RestauranteDao();
        Restaurante r = new Restaurante();
        
        List<Restaurante> restaurantes;
        restaurantes = rDao.buscarPorNome(fieldBusca.getText());
        
        ((DefaultTableModel) tabela.getModel()).setNumRows(0);
        
        r.setNome(fieldBusca.getText());
        
        for (Restaurante restaurante : restaurantes) {
            Object[] linha = new Object[5];
            linha[0] = restaurante.getId();
            linha[1] = restaurante.getTipo();
            linha[2] = restaurante.getNome();
            linha[3] = restaurante.getEndereco();
            linha[4] = restaurante.getContato();
            ((DefaultTableModel) tabela.getModel()).addRow(linha);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

