    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (!fieldNome.getText().isEmpty()) {
            AlunoDao aDao = new AlunoDao();
            aDao.inserir(fieldNome.getText(), fieldCpf.getText(), fieldEndereco.getText(), fieldContato.getText());

            ((DefaultTableModel) table.getModel()).setNumRows(0);

            List<Aluno> alunos = aDao.listar();

            for (Aluno aluno : alunos) {
                Object[] linha = new Object[5];
                linha[0] = aluno.getMatricula();
                linha[1] = aluno.getNome();
                linha[2] = aluno.getCpf();
                linha[3] = aluno.getEndereco();
                linha[4] = aluno.getContato();
                ((DefaultTableModel) table.getModel()).addRow(linha);
            }

            JOptionPane.showMessageDialog(null, "O aluno " + fieldNome.getText() + " foi cadastrado com sucesso!");

            fieldNome.setText("");
            fieldCpf.setText("");
            fieldEndereco.setText("");
            fieldContato.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "O aluno deve ter pelo menos o nome preenchido!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

