    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (table.getSelectedRow() >= 0) {
            AlunoDao aDao = new AlunoDao();
            aDao.excluir((int) ((DefaultTableModel) table.getModel()).getValueAt(table.getSelectedRow(), 0));

            ((DefaultTableModel) table.getModel()).removeRow(table.getSelectedRow());

            JOptionPane.showMessageDialog(null, "O(a) aluno(a) foi apagado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (isNumeric(fieldSearch.getText())) {
            AlunoDao aDao = new AlunoDao();

            ((DefaultTableModel) table.getModel()).setNumRows(0);

            List<Aluno> alunos = aDao.buscarPorMatricula(Integer.parseInt(fieldSearch.getText()));
            for (Aluno aluno : alunos) {
                Object[] linha = new Object[5];
                linha[0] = aluno.getMatricula();
                linha[1] = aluno.getNome();
                linha[2] = aluno.getCpf();
                linha[3] = aluno.getEndereco();
                linha[4] = aluno.getContato();
                ((DefaultTableModel) table.getModel()).addRow(linha);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite um número!");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

