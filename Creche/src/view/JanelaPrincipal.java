    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        AlunoDao aDao = new AlunoDao();
        Aluno a = new Aluno();

        a.setId((Long) ((DefaultTableModel) tabela.getModel()).getValueAt(tabela.getSelectedRow(), 0));

        aDao.excluir(a);

        ((DefaultTableModel) tabela.getModel()).removeRow(tabela.getSelectedRow());
        
        JOptionPane.showMessageDialog(null, "O(a) aluno(a) " + tabela.getValueAt(tabela.getSelectedRow(), 1).toString() + " foi apagado(a) com sucesso!!");
    }//GEN-LAST:event_btnExcluirActionPerformed

