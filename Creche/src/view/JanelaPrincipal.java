    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        AlunoDao aDao = new AlunoDao();
        Aluno a = new Aluno();
        
        a.setId(Integer.parseInt(fieldCodigo.getText()));
        a.setNome(fieldNome.getText());
        a.setDtNasc(fieldDtNasc.getText());
        a.setNomeMae(fieldNomeMae.getText());
        
        aDao.inserir(a);
        
        ((DefaultTableModel) tabela.getModel()).setNumRows(0);
        
        List<Aluno> alunos = aDao.listar();
        
        for (Aluno aluno : alunos) {
            Object[] linha = new Object[4];
            linha[0] = aluno.getId();
            linha[1] = aluno.getNome();
            linha[2] = aluno.getDtNasc();
            linha[3] = aluno.getNomeMae();
            ((DefaultTableModel)tabela.getModel()).addRow(linha);
        }
        
        JOptionPane.showMessageDialog(null, "O(a) aluno(a) " + fieldNome.getText() + " foi inserido(a) com sucesso!!");
        
        fieldCodigo.setText("");
        fieldNome.setText("");
        fieldDtNasc.setText("");
        fieldNomeMae.setText("");
    }//GEN-LAST:event_btnInserirActionPerformed

